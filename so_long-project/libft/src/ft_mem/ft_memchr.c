#include "../../include/libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)s)[i] == (unsigned char)c)
			return ((void *)(s + i));
		i++;
	}
	return (0);
}
/*
"ft_memchr" fonksiyonu, verilen bir bellek alanı içinde belirli 
bir karakter arar ve bu karakterin ilk olarak bulunduğu adresi döndürür.
*/